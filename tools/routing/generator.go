const routeGen string = `// Code generated by route-codegen. DO NOT EDIT.

package {{ .PackageName }}

{{ template "Imports" .Imports }}
{{ template "StaticAssetsVar" .StaticAssets }}

{{ template "RouteParams" .RouteParams }}

{{ template "SSEEventTypes" .SSEEventTypes }}

{{ template "HandlerInterface" .HandlerInterface }}

{{ template "MiddlewareRegistry" .MiddlewareRegistry }}

{{ template "RouterStruct" .RouterStruct }}

{{ template "MiddlewareHelpers" .MiddlewareHelpers }}

{{ template "RegisterRoutes" .RegisterRoutes }}

{{ template "RouteHandlers" .RouteHandlers }}

{{ template "WrapMiddleware" .WrapMiddleware }}
`

const pathsGen string = `// Code generated by route-codegen. DO NOT EDIT.

package {{ .PackageName }}

{{ template "RouteBuilders" .RouteBuilders }}
{{ template "StaticPathMethods" .StaticAssets }}
`

var routeGenTempl *template.Template
var pathsGenTempl *template.Template

func init() {
	routeGenTempl = template.Must(template.New("RouteGen").Parse(routeGen))
	template.Must(routeGenTempl.New("Imports").Parse(imports))
	template.Must(routeGenTempl.New("RouteParams").Parse(routeParams))
	sseEventTypesTempl := template.Must(routeGenTempl.New("SSEEventTypes").Parse(sseEventTypes))
	sseEventTypeTempl := template.Must(sseEventTypesTempl.New("SSEEventType").Parse(sseEventType))
	template.Must(sseEventTypeTempl.New("SSEEventKind").Parse(sseEventKind))
	handlerInterfaceTempl := template.Must(routeGenTempl.New("HandlerInterface").Parse(handlerInterface))
	template.Must(handlerInterfaceTempl.New("HandlerInterfaceMethod").Parse(handlerInterfaceMethod))
	template.Must(routeGenTempl.New("MiddlewareRegistry").Parse(middlewareRegistry))
	template.Must(routeGenTempl.New("RouterStruct").Parse(routerStruct))
	template.Must(routeGenTempl.New("MiddlewareHelpers").Parse(middlewareHelpers))
	registerRoutesTempl := template.Must(routeGenTempl.New("RegisterRoutes").Parse(registerRoutes))
	template.Must(registerRoutesTempl.New("MiddlewareGroup").Parse(middlewareGroup))
	template.Must(registerRoutesTempl.New("RegisterRoute").Parse(registerRoute))
	routeHandlersTempl := template.Must(routeGenTempl.New("RouteHandlers").Parse(routeHandlers))
	routeHandlerTempl := template.Must(routeHandlersTempl.New("RouteHandler").Parse(routeHandler))
	template.Must(routeHandlerTempl.New("RouteSSEEvent").Parse(routeSSEEvent))
	template.Must(routeGenTempl.New("WrapMiddleware").Parse(wrapMiddleware))
	template.Must(routeGenTempl.New("StaticAssetsVar").Parse(staticAssetsVar))

	routeGroupFuncs := template.FuncMap{
		"lower":      strings.ToLower,
		"capitalize": capitalize,
	}
	pathsGenTempl = template.Must(template.New("PathsGen").Funcs(routeGroupFuncs).Parse(pathsGen))
	routeBuildersTempl := template.Must(pathsGenTempl.New("RouteBuilders").Parse(routeBuilders))
	template.Must(routeBuildersTempl.New("RouteGroup").Funcs(routeGroupFuncs).Parse(routeGroup))
	template.Must(pathsGenTempl.New("StaticPathMethods").Parse(staticPathMethods))
}

type RouteGen struct {
	Imports            Imports
	RouteParams        RouteParams
	SSEEventTypes      SSEEventTypes
	HandlerInterface   HandlerInterface
	MiddlewareRegistry MiddlewareRegistry
	RouterStruct       RouterStruct
	MiddlewareHelpers  MiddlewareHelpers
	RegisterRoutes     RegisterRoutes
	RouteHandlers      RouteHandlers
	WrapMiddleware     WrapMiddleware
	StaticAssets       StaticAssets
	PackageName        string
}

type PathsGenData struct {
	RouteBuilders RouteBuilders
	StaticAssets  StaticAssets
	PackageName   string
}

// Generate creates the Go code from the route configuration
func Generate(config *RouteConfig, packageName string, staticFiles []StaticFileInfo) (string, error) {
	var buf bytes.Buffer

	assets := StaticAssets{Files: staticFiles, HasFiles: len(staticFiles) > 0}
	route := RouteGen{
		PackageName:        packageName,
		Imports:            GetImports(config, staticFiles),
		RouteParams:        GetRouteParams(config),
		SSEEventTypes:      GetSSEEventTypes(config),
		HandlerInterface:   GetHandlerInterface(config),
		MiddlewareRegistry: GetMiddlewareRegistry(config),
		RouterStruct:       GetRouterStruct(config),
		MiddlewareHelpers:  GetMiddlewareHelpers(config),
		RegisterRoutes:     GetRegisterRoutes(config, staticFiles),
		RouteHandlers:      GetRouteHandlers(config),
		WrapMiddleware:     GetWrapMiddleware(config),
		StaticAssets:       assets,
	}

	if err := routeGenTempl.Execute(&buf, route); err != nil {
		return "", err
	}

	// Format the generated code
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Return unformatted code for debugging
		return buf.String(), fmt.Errorf("failed to format generated code: %w", err)
	}

	return string(formatted), nil
}

// GeneratePaths creates the Go code for type-safe route builders
func GeneratePaths(config *RouteConfig, packageName string, staticFiles []StaticFileInfo) (string, error) {
	var buf bytes.Buffer

	data := PathsGenData{
		PackageName:   packageName,
		RouteBuilders: GetRouteBuilders(config),
		StaticAssets:  StaticAssets{Files: staticFiles, HasFiles: len(staticFiles) > 0},
	}

	if err := pathsGenTempl.Execute(&buf, data); err != nil {
		return "", err
	}

	// Format the generated code
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Return unformatted code for debugging
		return buf.String(), fmt.Errorf("failed to format generated code: %w", err)
	}

	return string(formatted), nil
}

// collectImports gathers all unique template package imports
func collectImports(config *RouteConfig) map[string]string {
	imports := make(map[string]string)

	addImport := func(pkg string) {
		if pkg != "" {
			// Create a short alias from the last part of the package path
			alias := filepath.Base(pkg)
			// Handle potential conflicts by using full path segments if needed
			if existing, exists := imports[alias]; exists && existing != pkg {
				// Create a more unique alias
				parts := strings.Split(pkg, "/")
				if len(parts) >= 2 {
					alias = strings.Join(parts[len(parts)-2:], "_")
				}
			}
			imports[alias] = pkg
		}
	}

	for _, route := range config.Routes {
		for _, method := range route.Methods {
			// Add regular template package
			addImport(method.Template.Package)

			// Add SSE event template packages
			for _, event := range method.Events {
				addImport(event.Template.Package)
			}
		}
	}
	return imports
}

func getEventInterfaceName(method *RouteMethod) string {
	return fmt.Sprintf("%sEvent", method.Handler)
}

// pathToStructName converts a path to a valid Go struct name
// e.g., "/posts/{id}" -> "PostsId"
// e.g., "/api/protected/posts/{id}/comments" -> "ApiProtectedPostsIdComments"
func pathToStructName(path string) string {
	// Remove leading slash
	path = strings.TrimPrefix(path, "/")

	// Split by slash
	segments := strings.Split(path, "/")

	var parts []string
	for _, segment := range segments {
		// Remove curly braces from parameters
		segment = strings.Trim(segment, "{}")
		if segment != "" {
			parts = append(parts, ToPascalCase(segment))
		}
	}

	return strings.Join(parts, "")
}

// toKebabCase converts a PascalCase or camelCase string to kebab-case
func toKebabCase(s string) string {
	var result strings.Builder
	for i, r := range s {
		if i > 0 && r >= 'A' && r <= 'Z' {
			result.WriteRune('-')
		}
		result.WriteRune(r)
	}
	return strings.ToLower(result.String())
}

// getTemplateAlias returns the import alias for a template package
func getTemplateAlias(pkg string, imports map[string]string) string {
	for alias, p := range imports {
		if p == pkg {
			return alias
		}
	}
	return filepath.Base(pkg)
}

// capitalize returns the string with the first letter capitalized
func capitalize(s string) string {
	if len(s) == 0 {
		return s
	}
	runes := []rune(s)
	runes[0] = unicode.ToUpper(runes[0])
	return string(runes)
}
