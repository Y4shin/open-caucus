// capitalize returns the string with the first letter capitalized
func capitalize(s string) string {
	if len(s) == 0 {
		return s
	}
	runes := []rune(s)
	runes[0] = unicode.ToUpper(runes[0])
	return string(runes)
}

// Generate creates the Go code from the route configuration
func Generate(config *RouteConfig, packageName string) (string, error) {
	var buf bytes.Buffer

	// Write package and imports
	buf.WriteString(fmt.Sprintf("// Code generated by route-codegen. DO NOT EDIT.\n\n"))
	buf.WriteString(fmt.Sprintf("package %s\n\n", packageName))

	// Collect all unique template imports
	imports := collectImports(config)

	// Check if we have any SSE routes (they need bytes and fmt)
	hasSSE := false
	for _, route := range config.Routes {
		for _, method := range route.Methods {
			if method.SSE {
				hasSSE = true
				break
			}
		}
		if hasSSE {
			break
		}
	}

	// Always include basic imports needed for Router, even with no routes
	buf.WriteString("import (\n")
	if hasSSE {
		buf.WriteString("\t\"bytes\"\n")
	}
	if len(imports) > 0 {
		buf.WriteString("\t\"context\"\n")
	}
	if hasSSE {
		buf.WriteString("\t\"fmt\"\n")
	}
	buf.WriteString("\t\"net/http\"\n")
	buf.WriteString("\t\"strings\"\n")

	if len(imports) > 0 {
		buf.WriteString("\n")
		for alias, pkg := range imports {
			buf.WriteString(fmt.Sprintf("\t%s \"%s\"\n", alias, pkg))
		}
	}
	buf.WriteString(")\n\n")

	// Generate RouteParams struct
	if err := generateRouteParams(&buf, config); err != nil {
		return "", err
	}

	// Generate SSE event types
	if err := generateSSEEventTypes(&buf, config, imports); err != nil {
		return "", err
	}

	// Generate Handler interface
	if err := generateHandlerInterface(&buf, config, imports); err != nil {
		return "", err
	}

	// Generate MiddlewareRegistry interface
	generateMiddlewareRegistry(&buf)

	// Generate Router struct
	generateRouterStruct(&buf)

	// Generate middleware helper types and functions
	generateMiddlewareHelpers(&buf, config)

	// Generate RegisterRoutes method
	if err := generateRegisterRoutes(&buf, config); err != nil {
		return "", err
	}

	// Generate individual route handlers
	if err := generateRouteHandlers(&buf, config, imports); err != nil {
		return "", err
	}

	// Generate wrapMiddleware function
	generateWrapMiddleware(&buf)

	// Generate type-safe route builders
	if err := generateRouteBuilders(&buf, config); err != nil {
		return "", err
	}

	// Format the generated code
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Return unformatted code for debugging
		return buf.String(), fmt.Errorf("failed to format generated code: %w", err)
	}

	return string(formatted), nil
}

// collectImports gathers all unique template package imports
func collectImports(config *RouteConfig) map[string]string {
	imports := make(map[string]string)

	addImport := func(pkg string) {
		if pkg != "" {
			// Create a short alias from the last part of the package path
			alias := filepath.Base(pkg)
			// Handle potential conflicts by using full path segments if needed
			if existing, exists := imports[alias]; exists && existing != pkg {
				// Create a more unique alias
				parts := strings.Split(pkg, "/")
				if len(parts) >= 2 {
					alias = strings.Join(parts[len(parts)-2:], "_")
				}
			}
			imports[alias] = pkg
		}
	}

	for _, route := range config.Routes {
		for _, method := range route.Methods {
			// Add regular template package
			addImport(method.Template.Package)

			// Add SSE event template packages
			for _, event := range method.Events {
				addImport(event.Template.Package)
			}
		}
	}
	return imports
}

// generateRouteParams creates the RouteParams struct with all path parameters
func generateRouteParams(buf *bytes.Buffer, config *RouteConfig) error {
	// Collect all unique path parameters
	paramSet := make(map[string]bool)
	for _, route := range config.Routes {
		params := ExtractPathParams(route.Path)
		for _, param := range params {
			paramSet[param] = true
		}
	}

	if len(paramSet) == 0 {
		// No parameters, skip struct generation
		return nil
	}

	// Sort for deterministic output
	var params []string
	for param := range paramSet {
		params = append(params, param)
	}
	sort.Strings(params)

	buf.WriteString("// RouteParams contains extracted parameters from the URL path\n")
	buf.WriteString("type RouteParams struct {\n")
	for _, param := range params {
		buf.WriteString(fmt.Sprintf("\t%s string\n", ToPascalCase(param)))
	}
	buf.WriteString("}\n\n")

	return nil
}

// generateSSEEventTypes creates event type interfaces and structs for SSE handlers
func generateSSEEventTypes(buf *bytes.Buffer, config *RouteConfig, imports map[string]string) error {
	// Track which SSE handlers we've generated types for
	generatedHandlers := make(map[string]bool)

	for _, route := range config.Routes {
		for _, method := range route.Methods {
			if !method.SSE || len(method.Events) == 0 {
				continue
			}

			// Skip if we've already generated for this handler
			if generatedHandlers[method.Handler] {
				continue
			}
			generatedHandlers[method.Handler] = true

			eventInterfaceName := fmt.Sprintf("%sEvent", method.Handler)

			// Generate the event interface
			buf.WriteString(fmt.Sprintf("// %s is the union type for all %s SSE events\n", eventInterfaceName, method.Handler))
			buf.WriteString(fmt.Sprintf("type %s interface {\n", eventInterfaceName))
			buf.WriteString(fmt.Sprintf("\tIs%s()\n", eventInterfaceName))
			buf.WriteString("}\n\n")

			// Generate concrete event types for each event
			for _, event := range method.Events {
				eventTypeName := fmt.Sprintf("%sEvent", event.Name)
				templateAlias := getTemplateAlias(event.Template.Package, imports)
				inputType := fmt.Sprintf("%s.%s", templateAlias, event.Template.InputType)

				// Generate the struct
				buf.WriteString(fmt.Sprintf("// %s represents a %s event\n", eventTypeName, event.Name))
				buf.WriteString(fmt.Sprintf("type %s struct {\n", eventTypeName))
				buf.WriteString(fmt.Sprintf("\tData %s\n", inputType))
				buf.WriteString("}\n\n")

				// Generate the interface method
				buf.WriteString(fmt.Sprintf("func (e %s) Is%s() {}\n\n", eventTypeName, eventInterfaceName))
			}
		}
	}

	return nil
}

// generateHandlerInterface creates the Handler interface with all methods
func generateHandlerInterface(buf *bytes.Buffer, config *RouteConfig, imports map[string]string) error {
	buf.WriteString("// Handler interface with all route methods\n")
	buf.WriteString("type Handler interface {\n")

	for _, route := range config.Routes {
		for _, method := range route.Methods {
			params := ExtractPathParams(route.Path)

			// All handlers start with context.Context as first parameter
			buf.WriteString(fmt.Sprintf("\t%s(ctx context.Context", method.Handler))

			if method.SSE {
				// SSE handlers take request but not response writer
				buf.WriteString(", r *http.Request")
				if len(params) > 0 {
					buf.WriteString(", params RouteParams")
				}
				// SSE handlers return a channel of events
				buf.WriteString(fmt.Sprintf(") (<-chan %sEvent, error)\n", method.Handler))
			} else {
				// Regular handlers take response writer and request
				buf.WriteString(", w http.ResponseWriter, r *http.Request")
				if len(params) > 0 {
					buf.WriteString(", params RouteParams")
				}
				templateAlias := getTemplateAlias(method.Template.Package, imports)
				inputType := fmt.Sprintf("%s.%s", templateAlias, method.Template.InputType)
				buf.WriteString(fmt.Sprintf(") (*%s, *ResponseMeta, error)\n", inputType))
			}
		}
	}

	buf.WriteString("}\n\n")
	return nil
}

// generateMiddlewareRegistry creates the MiddlewareRegistry interface
func generateMiddlewareRegistry(buf *bytes.Buffer) {
	buf.WriteString("// MiddlewareRegistry provides middleware functions by name\n")
	buf.WriteString("type MiddlewareRegistry interface {\n")
	buf.WriteString("\tGet(name string) func(http.Handler) http.Handler\n")
	buf.WriteString("}\n\n")
}

// generateRouterStruct creates the Router struct
func generateRouterStruct(buf *bytes.Buffer) {
	buf.WriteString("// Router wraps the handler and provides route registration\n")
	buf.WriteString("type Router struct {\n")
	buf.WriteString("\thandler    Handler\n")
	buf.WriteString("\tmiddleware MiddlewareRegistry\n")
	buf.WriteString("\tmux        *http.ServeMux\n")
	buf.WriteString("}\n\n")

	buf.WriteString("func NewRouter(handler Handler, middleware MiddlewareRegistry) *Router {\n")
	buf.WriteString("\treturn &Router{\n")
	buf.WriteString("\t\thandler:    handler,\n")
	buf.WriteString("\t\tmiddleware: middleware,\n")
	buf.WriteString("\t\tmux:        http.NewServeMux(),\n")
	buf.WriteString("\t}\n")
	buf.WriteString("}\n\n")
}

// generateMiddlewareHelpers creates middleware group types and helper functions
func generateMiddlewareHelpers(buf *bytes.Buffer, config *RouteConfig) {
	buf.WriteString("// middlewareGroup defines middleware applied to path prefixes\n")
	buf.WriteString("type middlewareGroup struct {\n")
	buf.WriteString("\tprefix     string\n")
	buf.WriteString("\tmiddleware []string\n")
	buf.WriteString("}\n\n")

	buf.WriteString("// getMiddlewareForPath returns all middleware that should apply to a path\n")
	buf.WriteString("// by matching against all prefix groups\n")
	buf.WriteString("func getMiddlewareForPath(path string, groups []middlewareGroup) []string {\n")
	buf.WriteString("\tvar middleware []string\n\n")
	buf.WriteString("\t// Apply middleware from all matching prefix groups\n")
	buf.WriteString("\tfor _, group := range groups {\n")
	buf.WriteString("\t\tif strings.HasPrefix(path, group.prefix) {\n")
	buf.WriteString("\t\t\tmiddleware = append(middleware, group.middleware...)\n")
	buf.WriteString("\t\t}\n")
	buf.WriteString("\t}\n\n")
	buf.WriteString("\treturn middleware\n")
	buf.WriteString("}\n\n")
}

// generateRegisterRoutes creates the RegisterRoutes method
func generateRegisterRoutes(buf *bytes.Buffer, config *RouteConfig) error {
	buf.WriteString("// RegisterRoutes installs all routes into the HTTP server\n")
	buf.WriteString("func (rt *Router) RegisterRoutes() http.Handler {\n")

	// Generate middleware groups
	if len(config.MiddlewareGroups) > 0 {
		buf.WriteString("\t// Define middleware groups from YAML\n")
		buf.WriteString("\tgroups := []middlewareGroup{\n")

		// Sort by prefix length (longer first) for correct matching order
		sortedGroups := make([]MiddlewareGroup, len(config.MiddlewareGroups))
		copy(sortedGroups, config.MiddlewareGroups)
		sort.Slice(sortedGroups, func(i, j int) bool {
			return len(sortedGroups[i].Prefix) > len(sortedGroups[j].Prefix)
		})

		for _, group := range sortedGroups {
			buf.WriteString(fmt.Sprintf("\t\t{prefix: %q, middleware: []string{", group.Prefix))
			for i, mw := range group.Middleware {
				if i > 0 {
					buf.WriteString(", ")
				}
				buf.WriteString(fmt.Sprintf("%q", mw))
			}
			buf.WriteString("}},\n")
		}
		buf.WriteString("\t}\n\n")
	} else {
		buf.WriteString("\tgroups := []middlewareGroup{}\n\n")
	}

	// Register each route
	if len(config.Routes) == 0 {
		buf.WriteString("\t_ = groups // suppress unused variable warning when no routes defined\n\n")
	}

	for _, route := range config.Routes {
		for _, method := range route.Methods {
			verb := strings.ToUpper(method.Verb)
			handlerFunc := fmt.Sprintf("rt.handle%s", method.Handler)

			buf.WriteString(fmt.Sprintf("\trt.mux.HandleFunc(%q, rt.wrapMiddleware(\n", fmt.Sprintf("%s %s", verb, route.Path)))
			buf.WriteString(fmt.Sprintf("\t\t%s,\n", handlerFunc))
			buf.WriteString(fmt.Sprintf("\t\tgetMiddlewareForPath(%q, groups),\n", route.Path))

			// Route-specific middleware
			if len(method.Middleware) > 0 {
				buf.WriteString("\t\t[]string{")
				for i, mw := range method.Middleware {
					if i > 0 {
						buf.WriteString(", ")
					}
					buf.WriteString(fmt.Sprintf("%q", mw))
				}
				buf.WriteString("},\n")
			} else {
				buf.WriteString("\t\t[]string{},\n")
			}

			// SSE flag
			buf.WriteString(fmt.Sprintf("\t\t%t,\n", method.SSE))
			buf.WriteString("\t))\n\n")
		}
	}

	buf.WriteString("\treturn rt.mux\n")
	buf.WriteString("}\n\n")

	return nil
}

// generateRouteHandlers creates individual handler functions
func generateRouteHandlers(buf *bytes.Buffer, config *RouteConfig, imports map[string]string) error {
	for _, route := range config.Routes {
		for _, method := range route.Methods {
			params := ExtractPathParams(route.Path)

			buf.WriteString(fmt.Sprintf("func (rt *Router) handle%s(w http.ResponseWriter, r *http.Request) {\n", method.Handler))

			// Extract path parameters if any
			if len(params) > 0 {
				buf.WriteString("\tparams := RouteParams{\n")
				for _, param := range params {
					buf.WriteString(fmt.Sprintf("\t\t%s: r.PathValue(%q),\n", ToPascalCase(param), param))
				}
				buf.WriteString("\t}\n\n")
			}

			if method.SSE {
				// SSE handlers return a channel of events
				// Call handler to get event channel
				if len(params) > 0 {
					buf.WriteString(fmt.Sprintf("\teventChan, err := rt.handler.%s(r.Context(), r, params)\n", method.Handler))
				} else {
					buf.WriteString(fmt.Sprintf("\teventChan, err := rt.handler.%s(r.Context(), r)\n", method.Handler))
				}
				buf.WriteString("\tif err != nil {\n")
				buf.WriteString("\t\thttp.Error(w, err.Error(), http.StatusInternalServerError)\n")
				buf.WriteString("\t\treturn\n")
				buf.WriteString("\t}\n\n")

				// Check for flusher support
				buf.WriteString("\tflusher, ok := w.(http.Flusher)\n")
				buf.WriteString("\tif !ok {\n")
				buf.WriteString("\t\thttp.Error(w, \"streaming not supported\", http.StatusInternalServerError)\n")
				buf.WriteString("\t\treturn\n")
				buf.WriteString("\t}\n\n")

				// Event streaming loop
				buf.WriteString("\tfor {\n")
				buf.WriteString("\t\tselect {\n")
				buf.WriteString("\t\tcase <-r.Context().Done():\n")
				buf.WriteString("\t\t\treturn\n")
				buf.WriteString("\t\tcase evt, ok := <-eventChan:\n")
				buf.WriteString("\t\t\tif !ok {\n")
				buf.WriteString("\t\t\t\treturn\n")
				buf.WriteString("\t\t\t}\n\n")

				// Type switch on event types
				buf.WriteString("\t\t\tswitch e := evt.(type) {\n")
				for _, event := range method.Events {
					eventTypeName := fmt.Sprintf("%sEvent", event.Name)
					templateAlias := getTemplateAlias(event.Template.Package, imports)
					templateFunc := fmt.Sprintf("%s.%s", templateAlias, event.Template.Type)

					// Convert event name to kebab-case for SSE event type
					eventName := toKebabCase(event.Name)

					buf.WriteString(fmt.Sprintf("\t\t\tcase %s:\n", eventTypeName))
					buf.WriteString("\t\t\t\tvar buf bytes.Buffer\n")
					buf.WriteString(fmt.Sprintf("\t\t\t\t%s(e.Data).Render(r.Context(), &buf)\n", templateFunc))
					buf.WriteString(fmt.Sprintf("\t\t\t\tfmt.Fprintf(w, \"event: %s\\ndata: %%s\\n\\n\", buf.String())\n", eventName))
					buf.WriteString("\t\t\t\tflusher.Flush()\n")
				}
				buf.WriteString("\t\t\t}\n")
				buf.WriteString("\t\t}\n")
				buf.WriteString("\t}\n")
			} else {
				templateAlias := getTemplateAlias(method.Template.Package, imports)
				templateFunc := fmt.Sprintf("%s.%s", templateAlias, method.Template.Type)

				// Regular handlers - pass context as first parameter
				if len(params) > 0 {
					buf.WriteString(fmt.Sprintf("\tinput, meta, err := rt.handler.%s(r.Context(), w, r, params)\n", method.Handler))
				} else {
					buf.WriteString(fmt.Sprintf("\tinput, meta, err := rt.handler.%s(r.Context(), w, r)\n", method.Handler))
				}

				// Error handling
				buf.WriteString("\tif err != nil {\n")
				buf.WriteString("\t\thttp.Error(w, err.Error(), http.StatusInternalServerError)\n")
				buf.WriteString("\t\treturn\n")
				buf.WriteString("\t}\n\n")

				// Set cookies and headers BEFORE redirect
				buf.WriteString("\t// Set cookies and headers\n")
				buf.WriteString("\tif meta != nil {\n")
				buf.WriteString("\t\tfor _, cookie := range meta.Cookies {\n")
				buf.WriteString("\t\t\thttp.SetCookie(w, cookie)\n")
				buf.WriteString("\t\t}\n")
				buf.WriteString("\t\tfor key, value := range meta.Headers {\n")
				buf.WriteString("\t\t\tw.Header().Set(key, value)\n")
				buf.WriteString("\t\t}\n")
				buf.WriteString("\t}\n\n")

				// Handle redirect if present
				buf.WriteString("\t// Handle redirect\n")
				buf.WriteString("\tif meta != nil && meta.Redirect != nil {\n")
				buf.WriteString("\t\thttp.Redirect(w, r, meta.Redirect.Location, meta.Redirect.StatusCode)\n")
				buf.WriteString("\t\treturn\n")
				buf.WriteString("\t}\n\n")

				// Call template with input
				buf.WriteString(fmt.Sprintf("\t%s(*input).Render(r.Context(), w)\n", templateFunc))
			}

			buf.WriteString("}\n\n")
		}
	}

	return nil
}

// generateWrapMiddleware creates the middleware wrapping function
func generateWrapMiddleware(buf *bytes.Buffer) {
	buf.WriteString("// wrapMiddleware chains middleware in the correct order\n")
	buf.WriteString("// prefixMiddleware comes from matching prefixes, routeMiddleware from the route definition\n")
	buf.WriteString("func (rt *Router) wrapMiddleware(\n")
	buf.WriteString("\thandler http.HandlerFunc,\n")
	buf.WriteString("\tprefixMiddleware []string,\n")
	buf.WriteString("\trouteMiddleware []string,\n")
	buf.WriteString("\tsse bool,\n")
	buf.WriteString(") http.HandlerFunc {\n")
	buf.WriteString("\treturn func(w http.ResponseWriter, r *http.Request) {\n")
	buf.WriteString("\t\t// Set SSE headers if needed\n")
	buf.WriteString("\t\tif sse {\n")
	buf.WriteString("\t\t\tw.Header().Set(\"Content-Type\", \"text/event-stream\")\n")
	buf.WriteString("\t\t\tw.Header().Set(\"Cache-Control\", \"no-cache\")\n")
	buf.WriteString("\t\t\tw.Header().Set(\"Connection\", \"keep-alive\")\n")
	buf.WriteString("\t\t}\n\n")

	buf.WriteString("\t\th := http.Handler(handler)\n\n")
	buf.WriteString("\t\t// Combine middleware: prefix middleware first, then route-specific\n")
	buf.WriteString("\t\tallMiddleware := append(prefixMiddleware, routeMiddleware...)\n\n")
	buf.WriteString("\t\t// Apply middleware in reverse order so they execute in declaration order\n")
	buf.WriteString("\t\tfor i := len(allMiddleware) - 1; i >= 0; i-- {\n")
	buf.WriteString("\t\t\tmiddleware := rt.middleware.Get(allMiddleware[i])\n")
	buf.WriteString("\t\t\tif middleware != nil {\n")
	buf.WriteString("\t\t\t\th = middleware(h)\n")
	buf.WriteString("\t\t\t}\n")
	buf.WriteString("\t\t}\n\n")
	buf.WriteString("\t\th.ServeHTTP(w, r)\n")
	buf.WriteString("\t}\n")
	buf.WriteString("}\n\n")
}

// pathToStructName converts a path to a valid Go struct name
// e.g., "/posts/{id}" -> "PostsId"
// e.g., "/api/protected/posts/{id}/comments" -> "ApiProtectedPostsIdComments"
func pathToStructName(path string) string {
	// Remove leading slash
	path = strings.TrimPrefix(path, "/")

	// Split by slash
	segments := strings.Split(path, "/")

	var parts []string
	for _, segment := range segments {
		// Remove curly braces from parameters
		segment = strings.Trim(segment, "{}")
		if segment != "" {
			parts = append(parts, ToPascalCase(segment))
		}
	}

	return strings.Join(parts, "")
}

// generateRouteBuilders creates type-safe route builder types
func generateRouteBuilders(buf *bytes.Buffer, config *RouteConfig) error {
	buf.WriteString("// Type-safe route builders\n")
	buf.WriteString("type Routes struct{}\n\n")
	buf.WriteString("var Route Routes\n\n")

	// For now, generate simple builders for routes with parameters
	// Group routes by their path structure
	routeGroups := groupRoutesByStructure(config)

	for _, group := range routeGroups {
		if err := generateRouteGroup(buf, group); err != nil {
			return err
		}
	}

	return nil
}

// routeGroup represents routes with similar structures
type routeGroup struct {
	basePath string
	params   []string
	routes   []Route
}

// groupRoutesByStructure groups routes by their path structure
func groupRoutesByStructure(config *RouteConfig) []routeGroup {
	groups := make(map[string]*routeGroup)

	for _, route := range config.Routes {
		params := ExtractPathParams(route.Path)
		// Create a key based on the path structure
		key := route.Path
		for _, param := range params {
			key = strings.Replace(key, "{"+param+"}", "{}", 1)
		}

		if groups[key] == nil {
			groups[key] = &routeGroup{
				basePath: key,
				params:   params,
			}
		}
		groups[key].routes = append(groups[key].routes, route)
	}

	// Convert map to slice
	var result []routeGroup
	for _, group := range groups {
		result = append(result, *group)
	}

	return result
}

// generateRouteGroup generates builder methods for a route group
func generateRouteGroup(buf *bytes.Buffer, group routeGroup) error {
	// For simple routes without parameters, just generate constant functions
	if len(group.params) == 0 {
		for _, route := range group.routes {
			for _, method := range route.Methods {
				methodName := fmt.Sprintf("%s%s", method.Handler, capitalize(strings.ToLower(method.Verb)))
				buf.WriteString(fmt.Sprintf("func (Routes) %s() string {\n", methodName))
				buf.WriteString(fmt.Sprintf("\treturn %q\n", route.Path))
				buf.WriteString("}\n\n")
			}
		}
		return nil
	}

	// For routes with parameters, generate builder types
	// Use the path structure to create a unique struct name
	for _, route := range group.routes {
		structName := pathToStructName(route.Path) + "Route"

		// Generate constructor
		buf.WriteString(fmt.Sprintf("type %s struct {\n", structName))
		for _, param := range group.params {
			buf.WriteString(fmt.Sprintf("\t%s string\n", param))
		}
		buf.WriteString("}\n\n")

		// Generate methods for each verb
		for _, method := range route.Methods {
			methodName := capitalize(strings.ToLower(method.Verb))
			buf.WriteString(fmt.Sprintf("func (r %s) %s() string {\n", structName, methodName))

			// Build the path with parameters
			path := route.Path
			for _, param := range group.params {
				path = strings.Replace(path, "{"+param+"}", "\" + r."+param+" + \"", 1)
			}
			path = strings.Replace(path, " + \"\"", "", -1)

			buf.WriteString(fmt.Sprintf("\treturn \"%s\"\n", path))
			buf.WriteString("}\n\n")
		}
	}

	return nil
}

// toKebabCase converts a PascalCase or camelCase string to kebab-case
func toKebabCase(s string) string {
	var result strings.Builder
	for i, r := range s {
		if i > 0 && r >= 'A' && r <= 'Z' {
			result.WriteRune('-')
		}
		result.WriteRune(r)
	}
	return strings.ToLower(result.String())
}

// getTemplateAlias returns the import alias for a template package
func getTemplateAlias(pkg string, imports map[string]string) string {
	for alias, p := range imports {
		if p == pkg {
			return alias
		}
	}
	return filepath.Base(pkg)
}
